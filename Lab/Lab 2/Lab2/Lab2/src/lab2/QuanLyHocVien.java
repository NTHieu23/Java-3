    public void loadTable() {
        String[] header = {"Họ Và Tên", "Điểm", "Khóa Học", "Xếp Loại", "Thưởng"};
        DefaultTableModel model = new DefaultTableModel(header, 0);

        for (HocVien hv : list) {
            Object[] row = {hv.getHoTen(), hv.getDiem(), hv.getKhoaHoc(), hv.xepLoai(), hv.isThuong() ? "Có" : "Không"};
            model.addRow(row);
        }
        tblQuanLyHocVien.setModel(model);
    }

    public void showDetail(int index) {
        HocVien hv = list.get(index);
        txtHoVaTen.setText(hv.getHoTen());
        txtDiem.setText(hv.getDiem() + "");
        txtXepLoai.setText(hv.xepLoai());
        chkCoPhanThuong.setSelected(hv.isThuong());

    }

