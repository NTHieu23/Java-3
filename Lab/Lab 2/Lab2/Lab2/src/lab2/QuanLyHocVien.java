    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        int index = tblQuanLyHocVien.getSelectedRow();
        if (index != -1) {
            list.remove(index);
            JOptionPane.showMessageDialog(this, "Xoa thanh cong");
            loadTable();
            return;
        } else {
            JOptionPane.showMessageDialog(this, "Xoa that bai");

        }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        // TODO add your handling code here:
        int index = tblQuanLyHocVien.getSelectedRow();

        if (index != -1) {
            String name = txtHoVaTen.getText();
            double diem = Double.parseDouble(txtDiem.getText());
            String khoaHoc = (String) cboKhoaHoc.getSelectedItem();

            HocVien hv = list.get(index);
            hv.setHoTen(name);
            hv.setDiem(diem);
            hv.setKhoaHoc(khoaHoc);

            loadTable();
            JOptionPane.showMessageDialog(rootPane, "Update thanh cong");
            return;
        } else {
            JOptionPane.showMessageDialog(rootPane, "Update that bai");

        }
    }//GEN-LAST:event_btnCapNhatActionPerformed

